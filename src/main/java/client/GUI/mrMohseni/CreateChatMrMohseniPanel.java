  private void chatsButtonActionPerformed(java.awt.event.ActionEvent evt) {
    client.changePanel(PanelName.MESSENGER_PANEL, UserRole.MR_MOHSENI);
  }

  private void sendTextButtonActionPerformed(java.awt.event.ActionEvent evt) {
    if (textField.getText().equals("")) {
      return;
    }
    ArrayList<String> contacts = new ArrayList<>();
    if (sendToAllBox.isSelected()) {
      for (int i = 0; i < contactsTable.getRowCount(); i++) {
        contacts.add((String) contactsTable.getValueAt(i, 0));
      }
      client.messengerSendText(textField.getText(), contacts);
    } else {
      String enteringYear = "";
      if (enteringYearBox.isSelected()) {
        enteringYear = enteringYearField.getText();
        try {
          Integer.parseInt(enteringYear);
        } catch (Exception e) {
          return;
        }
      }
      client.messengerSendText(textField.getText(), Objects.requireNonNull(facultyBox.getSelectedItem()).toString(),
              Objects.requireNonNull(gradeBox.getSelectedItem()).toString(), enteringYear);
    }
    textField.setText("");
  }

  private void sendFileButtonActionPerformed(java.awt.event.ActionEvent evt) {
    JFileChooser fileChooser = new JFileChooser(getConfig().getProperty(String.class, "fileChooserOpenDefaultDir"));
    int response = fileChooser.showOpenDialog(null);
    if (response == JFileChooser.APPROVE_OPTION) {
      File file = new File(fileChooser.getSelectedFile().getAbsolutePath());
      String fileName = file.getName();
      FileInputStream fileInputStream = null;
      try {
        fileInputStream = new FileInputStream(file);
      } catch (FileNotFoundException e) {
        e.printStackTrace();
      }
      byte[] bytes = new byte[(int) file.length()];
      try {
        int result;
        do {
          assert fileInputStream != null;
          result = fileInputStream.read(bytes);
        } while (result != -1);
        fileInputStream.close();
      } catch (IOException e) {
        e.printStackTrace();
      }

      ArrayList<String> contacts = new ArrayList<>();
      if (sendToAllBox.isSelected()) {
        for (int i = 0; i < contactsTable.getRowCount(); i++) {
          contacts.add((String) contactsTable.getValueAt(i, 0));
        }
        client.messengerSendFile(bytes, fileName, contacts);
      } else {
        String enteringYear = "";
        if (enteringYearBox.isSelected()) {
          enteringYear = enteringYearField.getText();
          try {
            Integer.parseInt(enteringYear);
          } catch (Exception e) {
            return;
          }
        }
        client.messengerSendFile(bytes, fileName, Objects.requireNonNull(facultyBox.getSelectedItem()).toString(),
                Objects.requireNonNull(gradeBox.getSelectedItem()).toString(), enteringYear);
      }
    }
  }

  private void showData(String[][] data) {
    String[] cols = {"id", "name", "faculty"};
    DefaultTableModel model = (DefaultTableModel) contactsTable.getModel();
    model.setDataVector(data, cols);
    contactsTable.setModel(model);
  }

  public void update(String[] faculties, String[][] data) {
    facultyBox.setModel(new javax.swing.DefaultComboBoxModel<>(faculties));
    String[][] strings = new String[data.length][3];
    for (int i = 0; i < data.length; i++) {
      strings[i][0] = data[i][0];
      strings[i][1] = data[i][1];
      strings[i][2] = data[i][2];
    }
    showData(strings);
  }

