  public void update(String id, String melliCode, String faculty, String phoneNumber, String degree, String roomNumber) {
    idLabel.setText(String.format(getConfig().getProperty(String.class, "idLabelText"), id == null ? "-" : id));
    melliCodeLabel.setText(String.format(getConfig().getProperty(String.class, "melliCodeLabelText"), (melliCode == null || melliCode.equals("")) ? "-" : melliCode));
    facultyLabel.setText(String.format(getConfig().getProperty(String.class, "facultyLabelText"), (faculty == null || faculty.equals("")) ? " " : faculty));
    phoneLabel.setText(String.format(getConfig().getProperty(String.class, "phoneLabelText"), (phoneNumber == null || phoneNumber.equals("")) ? "-" : phoneNumber));
    degreeLabel.setText(String.format(getConfig().getProperty(String.class, "degreeLabelText"), degree == null ? "-" : degree));
    roomNumberLabel.setText(String.format(getConfig().getProperty(String.class, "roomNumberLabelText"), (roomNumber == null || roomNumber.equals("")) ? "-" : roomNumber));
  }

