  private void rejectButtonActionPerformed(java.awt.event.ActionEvent evt) {
    answerRequest(false);
  }

  private void acceptButtonActionPerformed(java.awt.event.ActionEvent evt) {
    answerRequest(true);
  }

  private void answerRequest(boolean accepted) {
    try {
      Integer.parseInt(idField.getText());
    } catch (Exception e) {
      mainFrame.showMessage(getConfig().getProperty(String.class, "requestIdNumErrorMessage"));
      return;
    }

    client.answerEducationalRequest(EducationalRequest.Type.minor, idField.getText(), accepted);
    idField.setText("");
  }

  private void backButtonActionPerformed(java.awt.event.ActionEvent evt) {
    client.changePanel(PanelName.MINOR_LIST_PANEL, null);
  }

